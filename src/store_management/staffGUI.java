    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //add data to database
        
        String txt1 = jTextField6.getText();
        String txt2 = jTextField2.getText();
        String txt3 = jTextField3.getText();
        String txt4 = jTextField4.getText();
        String txt5 = jTextField5.getText();
        String txt6 = "";
        
        if(jRadioButton1.isSelected()) {
           txt6 = "Male";
       }
       else if(jRadioButton2.isSelected()){
         txt6 = "Female";
       }
        staffDAL dal = new staffDAL();
        staffDTO dto = new staffDTO();
        dto.setId(txt1);
        dto.setName(txt2);
        dto.setRoll(txt3);
        dto.setGender(txt6);
        dto.setPhoneNum(txt4);
        dto.setAddress(txt5);
               
        if(dal.addStaff(dto) != 0) {
            JOptionPane.showMessageDialog(null, "Data is inserted");
            DefaultTableModel t = (DefaultTableModel)jTable2.getModel();
            t.setRowCount(0);
            show_Data();
            countStaff();
        
        }
        else {
            JOptionPane.showMessageDialog(null, "Please check your database");
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

