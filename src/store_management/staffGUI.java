    public ArrayList<staffDTO> getList() {
        ArrayList<staffDTO> ar = new ArrayList<staffDTO>();
        Connection conn = MyConnection.getConnection();
        ResultSet rs;
        Statement st;
        staffDTO sf;
         String sql = "select * from staff";
        try {
           
            st = conn.createStatement();
            rs = st.executeQuery(sql);
            while(rs.next()) {
                sf = new staffDTO(rs.getString("id"), rs.getString("staff_name"),rs.getString("staff_role"), rs.getString("gender"), rs.getString("phone"), rs.getString("address"));
                ar.add(sf);
            }
        } catch(Exception e) {
            e.printStackTrace();
        }
        return ar;
    }
    public void show_Data() {
        DefaultTableModel t = (DefaultTableModel)jTable2.getModel();
        ArrayList<staffDTO> a = getList();
        Object[] rows = new Object[6];
        for(int i = 0; i < a.size(); i++) {
            rows[0] = a.get(i).getId();
            rows[1] = a.get(i).getName();
            rows[2] = a.get(i).getRoll();
            rows[3] = a.get(i).getGender();
            rows[4] = a.get(i).getPhoneNum();
            rows[5] = a.get(i).getAddress() ;
            t.addRow(rows);
        }
    }
    public void countStaff() {
        Connection conn = MyConnection.getConnection();
        Statement st;
        ResultSet rs;
        int total = 0;
        String sql = "SELECT count(*) as 'total' FROM staff";
        try {
            st = conn.createStatement();
            rs = st.executeQuery(sql);
            while(rs.next()) {
                   total = rs.getInt(1);
            }
            jLabel6.setText("Total amount: " + Integer.toString(total));
            
        } catch (SQLException ex) {
            Logger.getLogger(staffGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
       
  }
  
    
