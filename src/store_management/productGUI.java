        public ArrayList<productDTO> userList() {
        //Generate a list to add data to the table grid
        ArrayList<productDTO> userList = new ArrayList<productDTO>();
        Connection conn = MyConnection.getConnection();
        String query = "select * from product";
        Statement st;
        ResultSet rs;
        productDTO u;
        try {
            st = conn.createStatement();
            rs = st.executeQuery(query);
            while(rs.next()) {
               u = new productDTO(rs.getString("idProduct"), rs.getString("ProductName"), rs.getString("QTy"), rs.getString("Date_d"), rs.getString("Price"));
               userList.add(u);
            }
        }catch(Exception e) {
            
        }
        
        return userList;
    }
    public void show_result() {
        ArrayList<productDTO> list = userList();
        DefaultTableModel table = (DefaultTableModel)jTable1.getModel(); 
        Object[] rows = new Object[5];
        for(int i = 0; i < list.size(); i++) {
            rows[0] = list.get(i).getProductId();
            rows[1] = list.get(i).getProductName();
            rows[2] = list.get(i).getDate();
            rows[3] = list.get(i).getQtys();
            rows[4] = list.get(i).getPrice();
            table.addRow(rows);
           
        }
    }
   public void totalAmount() {
       DefaultTableModel t = (DefaultTableModel)jTable1.getModel();
       int count = 0;
       for(int i = 0; i < t.getRowCount(); i++) {
           count++;
       }
      
       jTextField6.setText(String.valueOf(count));
   }
   

