    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //update the product
        productDTO p = new productDTO();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String d = sdf.format(date_chooser.getDate());
        p.setProductId(jTextField1.getText());
        p.setProductName(jTextField2.getText());
          p.setDate(d);
        p.setQtys(jTextField4.getText());
        p.setPrice(jTextField5.getText());
        productBLL b = new productBLL();
        try {
            if(b.updateData(p) != 0) {
                JOptionPane.showMessageDialog(null, "Data is updated");
                  DefaultTableModel t = (DefaultTableModel) jTable1.getModel();
                  t.setRowCount(0);
                  show_result();
            }
        } catch (SQLException ex) {
            Logger.getLogger(productGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

