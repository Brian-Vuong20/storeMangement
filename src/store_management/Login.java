    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        lbl_U.setVisible(false);//check the validation of the username and password
        lbl_P.setVisible(false);
        if(jTextField2.getText().equals("")) {
            lbl_U.setVisible(true);
        }
        if (String.valueOf(jPasswordField1.getPassword()).equals("")) {
            lbl_P.setVisible(true);
        }
        else {
            Connection conn = MyConnection.getConnection();
            PreparedStatement ps;
            //
            try {
                ps = conn.prepareStatement("select * from User_account where userName = ? and user_password = ?");
                ps.setString(1, jTextField2.getText());
                ps.setString(2, String.valueOf(jPasswordField1.getPassword()));
                ResultSet rs = ps.executeQuery();
                if(rs.next()) {
                    Menu m = new Menu();
                   
                    m.setVisible(true);
                    m.pack();
                    m.setLocationRelativeTo(null);

                    Menu.userName.setText("Welcome" + " " +jTextField2.getText());
                    Menu.jLabel7.setText("You login as " + jTextField2.getText());

                }
                else {
                    JOptionPane.showMessageDialog(null, "Sorry wrong user name or password");
                }
            } catch(SQLException e) {

            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

