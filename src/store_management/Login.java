    private void jPasswordField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPasswordField1KeyPressed

        if(evt.getKeyCode() == KeyEvent.VK_ENTER ) {
            Connection conn = MyConnection.getConnection();
            PreparedStatement ps;
            ResultSet rs;
            try {
                String sql = "select * from User_account where userName = ? and user_password = ?";
                ps = conn.prepareStatement(sql);
                ps.setString(1, jTextField2.getText());
                ps.setString(2, String.valueOf(jPasswordField1.getPassword()));
                rs = ps.executeQuery();
                if(rs.next()) {
                    Menu m = new Menu();
                    Menu.userName.setText("Welcome " + jTextField2.getText());
                    Menu.jLabel7.setText("You login as " + jTextField2.getText());
                    m.setVisible(true);
                    m.setLocationRelativeTo(null);
                    m.pack();
                }
                else {
                    JOptionPane.showMessageDialog(null, "Sorry wrong user name or password");
                }
            } catch(Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jPasswordField1KeyPressed

